CREATE OR REPLACE VIEW STAGING.STG_MEDICATIONS AS
SELECT
    TRY_TO_DATE("START") AS START_DATE,
    TRY_TO_DATE("STOP") AS STOP_DATE,
    "PATIENT" AS PATIENT_ID,
    "PAYER" AS PAYER_ID,
    "ENCOUNTER" AS ENCOUNTER_ID,
    "CODE" AS MEDICATION_CODE,
